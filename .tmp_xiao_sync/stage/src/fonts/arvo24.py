def get_ch(ch):
    oc = ord(ch)
    ioff = 2 * (oc - 32 + 1) if oc >= 32 and oc <= 87 else 0
    doff = ifb(_mvi[ioff : ])
    width = ifb(_mvfont[doff : ])
